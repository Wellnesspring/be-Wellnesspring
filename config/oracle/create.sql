CREATE TABLE Sport_Routine  (
                                id NUMBER GENERATED BY DEFAULT AS IDENTITY,
                                user_id VARCHAR(50) NOT NULL,
                                sport_routine_name varchar(255) NOT NULL
);

CREATE TABLE  Sport_Routine_List  (
                                      id NUMBER GENERATED BY DEFAULT AS IDENTITY,
                                      sport_routine_id NUMBER NOT NULL,
                                      sport_category_id NUMBER NOT NULL,
                                      set_count NUMBER NOT NULL,
                                      sport_count NUMBER NOT NULL,
                                      volume NUMBER NOT NULL,
