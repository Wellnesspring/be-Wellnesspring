CREATE TABLE users (
                       user_id VARCHAR2(50) primary KEY NOT NULL,
                       role NUMBER DEFAULT 0	NOT NULL,
                       user_pw varchar2(50) NOT NULL,
                       name varchar2(20) NOT NULL,
                       serial_num_f varchar2(7) NOT NULL,
                       serial_num_l varchar2(100) NOT NULL,
                       phone varchar2(15) NOT NULL,
                       height NUMBER NOT NULL,
                       weight NUMBER NOT NULL,
                       join_at timestamp DEFAULT SYSDATE NOT NULL,
                       alarm_agree varchar2(10) NOT NULL,
                       profileImg varchar2(2000) NOT NULL,
                       point NUMBER DEFAULT 1 NOT NULL
);

SELECT * FROM users;

CREATE TABLE Food_Category (
                               id NUMBER GENERATED BY DEFAULT AS IDENTITY,
                               food_name 	varchar2(255) NOT NULL,
                               kcal NUMBER NOT NULL
);

CREATE TABLE Food_Like (
                           id NUMBER GENERATED BY DEFAULT AS IDENTITY,
                           food_id NUMBER NOT NULL,
                           liked_at timestamp	DEFAULT SYSDATE	NOT NULL
);


CREATE TABLE Meal (
                      meal_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
                      meal_date DATE NOT NULL,
                      meal_time time NOT NULL
);


CREATE TABLE Meal_detail  (
                              id NUMBER GENERATED BY DEFAULT AS IDENTITY,
                              meal_id NUMBER NOT NULL,
                              food_id NUMBER NOT NULL,
                              meal varchar2(50) NOT NULL,
                              amount float NOT NULL
);

